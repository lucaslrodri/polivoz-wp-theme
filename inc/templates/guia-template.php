        <h3 class="title">Prioridade das seções na Homepage</h3>
        <p>Na página inicial (Homepage) as seções aparecem de acordo com o seu número de prioridade. Este número é um valor numérico de 1 a 9 e indica qual seção deve aparecer primeiro. Seções com menor valor de prioridade aparecem primeiro.</p>
        <p>Ex: Sendo uma homepage composta de três seções com prioridade <code>2</code>, <code>1</code> e <code>5</code>, respectivamente. Na homepage irá aparecer primeiro a seção <code>1</code>, depois a <code>2</code> e por último a <code>5</code>.</p>
        <p>Caso duas seções tenham o mesmo valor de prioridade o próprio sistema irá definir a ordem da seção, portanto caso queira que todas as seções aparecam em uma determinada ordem na homepage <strong>é recomendado que o todas as seções tenham valores de prioridades distintos</strong>.</p>
        <p>Ao criar uma nova seção o sistema irá definir automaticamente um valor para esse parâmetro, baseado no número de seções existentes. Caso o usuário deixe em branco este parâmetro, o sistema irá utilizar o número de prioridade como o número totais de seções mais 1.</p>
