        <h3 class="title">Notas musicais (Numeração de títulos)</h3>
        <p>Os títulos de seções, posts, galerias e outros itens. pode ser numerado por notas musicais. Essas notas aparecem antes do título e servem para decoração do mesmo.</p>
        <b>Adicionando uma nota customizada</b>
        <p>As notas musicais são geradas automaticamente pelo sistema, no entanto, é possível customizar a nota de um determinado item. Para isso, no campo <strong>Nota musical</strong>, escreva uma ou duas letras. Cada letra representa uma nota musical, conforme a tabela de notas musicais abaixo. Só são permitidas letras do alfabeto latino e sem acentuação. Ex: <code>f</code></p>
        <b>Deixando o sistema definir a nota musical</b>
        <p>Caso deixe o campo <strong>Nota Musical</strong> em branco o sistema irá selecionar uma nota musical automaticamente.</p>
        <b>Criando títulos sem notas musicais</b>
        <p>É possível criar títulos sem notas musicais, para isso utilize o código: <code>0</code>.';
