        <div class="inside">
        <h3 class="title">Shortcode da galeria</h3>
        <p>Shortcodes são pequenos códigos, quase que marcações, onde através de uma configuração prévia, o site irá exibir algo ou realizar uma ação no momento que o shortcode é lido pelo sistema. <a href="https://codex.wordpress.org/Shortcode" target="_blank">Para mais informações sobre Shortcode clique aqui (Em inglês).</a>Portanto neste campo deve-se colocar um Shortcode capaz de criar uma galeria.</p>
        <p>Existem alguns plugins que criam galerias ou albuns de imagens, videos, audio e outros itens através de um shortcode. O plugin de galerias recomendado para esse tema é o GMedia Gallery. Nele é possível criar shortcode de albuns e galerias. Para conseguir o shortcode de uma galeria ou album com este plugin, na página do GMedia Gallery procure por Galeria ou Álbum e copie o shortcode necessário. Ex: <code>[gm album=1]</code>. <a href="http://codeasily.com/wordpress-plugins/gmedia-gallery/" target="_blank">Para mais informações visite a página do plugin (Em inglês).</a></p>
        <p>O uso de Shortcodes criado pelo Gmedia Gallery não é obrigatório. Na prática qualquer Shortcode que gere uma galeria irá servir. Existem outros plugins de galeria famosos como o <a href="https://br.wordpress.org/plugins/nextgen-gallery/" target="_blank">NextGEN Gallery</a>, contudo devido a falta de testes, não há garantias de funcionamento com outros tipos de Shortcodes diferentes dos criados pelo Gmedia Gallery.</p>
